    private void inicializarComponentes(){
        con = new Conexion();
        pedidoData = new PedidoData(con);
        detallePedidoData = new DetallePedidoData(con);
        modeloPedidos = (DefaultTableModel) jtPedidos.getModel();
        modeloProductos = (DefaultTableModel) jtProductos.getModel();
        detalles = detallePedidoData.obtenerDetallesPedidos();
        for(Pedido pedido : pedidoData.obtenerPedidosActivos()){
            modeloPedidos.addRow(new Object[]{pedido.getMesero().getNombre(),pedido.getMesa().getId(),pedido.getMonto(),pedido.getFechaPedido(),pedido.getId()});
        }
    }
    
    public void limpiarTabla(JTable tabla){
        try {
            DefaultTableModel modelo=(DefaultTableModel) tabla.getModel();
            int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }

    private boolean pedidoSeleccionado(){
        boolean retorno=false;
        if(jtPedidos.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this, "Seleccione un pedido.");
        }
        else{
            retorno=true;
        }
        return retorno;
    }
    
