    private void jtPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPedidosMouseClicked
        limpiarTabla(jtProductos);
        List<Producto> productos = new ArrayList<>();
        for(DetallePedido detalle : detalles){
            if(Integer.parseInt(jtPedidos.getValueAt(jtPedidos.getSelectedRow(), 4).toString()) == detalle.getPedido().getId()){
                productos.add(detalle.getProducto());
            }
        }
        int cont=1;
        for(Producto producto : productos){
            modeloProductos.addRow(new Object[]{cont,producto.getNombre(),producto.getPrecio()});
            cont++;
        }
    }//GEN-LAST:event_jtPedidosMouseClicked

